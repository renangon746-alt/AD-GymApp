    private void botonModUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModUsuariosActionPerformed
        // TODO add your handling code here:
        try {
            String idStr = JOptionPane.showInputDialog(this, "Introduce el ID del usuario a modificar:");
            if (idStr == null || idStr.trim().isEmpty()) return;

            int id = Integer.parseInt(idStr);
            Usuario u = gu.buscarPorId(id);

            if (u != null) {
                String nuevoNombre = JOptionPane.showInputDialog(this, "Nuevo nombre:", u.getNombre());
                String edadStr = JOptionPane.showInputDialog(this, "Nueva edad:", u.getEdad());
                String pesoStr = JOptionPane.showInputDialog(this, "Nuevo peso:", u.getPeso());
                String alturaStr = JOptionPane.showInputDialog(this, "Nueva altura:", u.getAltura());

                if (nuevoNombre != null && edadStr != null && pesoStr != null && alturaStr != null) {
                    gu.modificarUsuario(id, nuevoNombre, Integer.parseInt(edadStr), 
                            Double.parseDouble(pesoStr), Double.parseDouble(alturaStr));
                    JOptionPane.showMessageDialog(this, "Usuario modificado correctamente.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ un usuario con ese ID.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al modificar usuario: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonModUsuariosActionPerformed

