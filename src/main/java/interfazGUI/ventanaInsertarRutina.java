    private void botonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarActionPerformed
        // TODO add your handling code here:
        try {
            // 1.  Comprobar campos no estan vacios y Recoger datos de los campos
            comprobarCampos();
            String nombre = fieldNombre.getText();
            int idUsu = Integer.parseInt(fieldIdUsu.getText());
            // 2. Insertar con el gestor
                   gr.insertar(nombre, idUsu);
            // 3. Mostrar confirmaci√≥n
            JOptionPane.showMessageDialog(this, "Rutina insertada correctamente", "Exito", INFORMATION_MESSAGE);
            // 4. Limpiar campos
            fieldNombre.setText("");
            fieldIdUsu.setText("");
            System.out.println("Total rutinas registradas: " + gr.getRutinas().size());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Hubo un error, asegurese de poner bien los datos", "Error", ERROR_MESSAGE);
        } catch (MyException ex) {
            JOptionPane.showMessageDialog(this, "Error al insertar rutina: " + ex.getMessage(), "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonInsertarActionPerformed

