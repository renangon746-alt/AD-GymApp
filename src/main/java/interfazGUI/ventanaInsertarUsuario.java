    private void botonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Comprobar campos no estan vacios y Recoger datos de los campos
            comprobarCampos();
            String nombre = fieldNombre.getText();
            int edad = Integer.parseInt(fieldEdad.getText());
            double peso = Double.parseDouble(fieldPeso.getText());
            double altura = Double.parseDouble(fieldAltura.getText());
            // 2. Insertar con el gestor
            gu.insertar(nombre, edad, peso, altura);
            // 3. Mostrar confirmaci√≥n
            JOptionPane.showMessageDialog(this, "Usuario insertado correctamente", "Exito", INFORMATION_MESSAGE);
            // 4. Limpiar campos
            fieldNombre.setText("");
            fieldEdad.setText("");
            fieldPeso.setText("");
            fieldAltura.setText("");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Hubo un error, asegurese de poner bien los datos", "Error", ERROR_MESSAGE);
        } catch (MyException ex) {
            JOptionPane.showMessageDialog(this, "Error al insertar usuario: " + ex.getMessage(), "Error", ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_botonInsertarActionPerformed

