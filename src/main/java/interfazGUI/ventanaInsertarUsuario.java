    private void comprobarCampos() {
        if (fieldNombre.getText().isEmpty() || fieldEdad.getText().isEmpty() || fieldPeso.getText().isEmpty() || fieldAltura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", ERROR_MESSAGE);
            return;
        }
    }
    
