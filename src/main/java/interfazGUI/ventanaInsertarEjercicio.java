    private void botonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarActionPerformed
        try {
            // 1. Comprobar campos no estan vacios y Recoger datos de los campos
            System.out.println("Total rutinas registradas: " + gr.getRutinas().size());
            comprobarCampos();
            String nombre = fieldNombre.getText();
            String grupoMuscular = fieldGrupoMuscular.getText();
            String desc = fieldDescripcion.getText();
            int idRutina = Integer.parseInt(fieldidRutina.getText());
            // 2. Insertar con el gestor
            ge.insertar(nombre, grupoMuscular, desc, idRutina);
            // 3. Mostrar confirmaci√≥n
            JOptionPane.showMessageDialog(this, "Ejercicio insertado correctamente", "Exito", INFORMATION_MESSAGE);
            // 4. Limpiar campos
            fieldNombre.setText("");
            fieldGrupoMuscular.setText("");
            fieldDescripcion.setText("");
            fieldidRutina.setText("");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Hubo un error, asegurese de poner bien los datos", "Error", ERROR_MESSAGE);
        } catch (MyException ex) {
            JOptionPane.showMessageDialog(this, "Error al insertar ejercicio: " + ex.getMessage(), "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonInsertarActionPerformed

