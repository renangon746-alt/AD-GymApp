    private void comprobarCampos() {
        if (fieldNombre.getText().isEmpty() || fieldGrupoMuscular.getText().isEmpty() || fieldDescripcion.getText().isEmpty() || fieldidRutina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", ERROR_MESSAGE);
            return;
        }
    }
    
