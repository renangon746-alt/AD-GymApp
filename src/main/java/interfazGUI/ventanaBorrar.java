    private void botonBorrarUsuarioPorIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarUsuarioPorIDActionPerformed
        // TODO add your handling code here:
            try {
                String input = JOptionPane.showInputDialog(this, "Introduzca el ID del usuario a borrar:");
                if (input == null) {
                    // Usuario pulsó Cancelar
                    return;
                }
                input = input.trim();
                if (input.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Debe introducir un ID válido.", "Error", ERROR_MESSAGE);
                    return;
                }

                int id = Integer.parseInt(input);

                // Confirmar antes de borrar
                int confirm = JOptionPane.showConfirmDialog(this,
                        "¿Está seguro de que desea borrar el usuario con ID " + id + "?",
                        "Confirmar borrado",
                        JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                    Usuario u = gu.buscarPorId(id); // opcional si quieres mostrar nombre antes de borrar
                    if (u != null) {
                        gu.borrar(id);
                        JOptionPane.showMessageDialog(this, "Usuario borrado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "No se encontró ningún usuario con ese ID.", "Error", ERROR_MESSAGE);
                    }
                }

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "El ID debe ser un número entero válido.", "Error", ERROR_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al borrar usuario: " + ex.getMessage(), "Error", ERROR_MESSAGE);
            }
    }//GEN-LAST:event_botonBorrarUsuarioPorIDActionPerformed

    private void botonBorrarRutinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarRutinaActionPerformed
        // TODO add your handling code here:
        try {
            String input = JOptionPane.showInputDialog(this, "Introduzca el ID de la rutina a borrar:");
            if (input == null) return; // cancelar
            input = input.trim();
            if (input.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe introducir un ID válido.", "Error", ERROR_MESSAGE);
                return;
            }

            int id = Integer.parseInt(input);
            int confirm = JOptionPane.showConfirmDialog(this,
                    "¿Está seguro de que desea borrar la rutina con ID " + id + "?",
                    "Confirmar borrado",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                if (gr.buscarPorId(id) != null) {
                    gr.borrar(id);
                    JOptionPane.showMessageDialog(this, "Rutina borrada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró ninguna rutina con ese ID.", "Error", ERROR_MESSAGE);
                }
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número entero válido.", "Error", ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al borrar rutina: " + ex.getMessage(), "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonBorrarRutinaActionPerformed

