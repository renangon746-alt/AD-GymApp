    private void botonBorrarRutina1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarRutina1ActionPerformed
        // TODO add your handling code here:
        try {
            String nombre = JOptionPane.showInputDialog(this, "Introduce el nombre del ejercicio a borrar:");

            if (nombre != null && !nombre.trim().isEmpty()) {
                // Buscar si existe
                Ejercicio encontrado = ge.buscarPorNombre(nombre);
                if (encontrado != null) {
                    // Confirmar borrado
                    int confirm = JOptionPane.showConfirmDialog(this, 
                            "¿Estás seguro de borrar el ejercicio \"" + nombre + "\"?",
                            "Confirmar borrado", JOptionPane.YES_NO_OPTION);

                    if (confirm == JOptionPane.YES_OPTION) {
                        ge.borrarPorNombre(nombre);
                        JOptionPane.showMessageDialog(this, "Ejercicio borrado correctamente.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró ningún ejercicio con ese nombre.", 
                            "Borrado fallido", JOptionPane.ERROR_MESSAGE);
                }
            } 
            // Si el usuario pulsa "Cancelar", simplemente no hace nada
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al borrar ejercicio: " + ex.getMessage(), 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonBorrarRutina1ActionPerformed

